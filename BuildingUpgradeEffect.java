public class BuildingUpgradeEffect {
    public int prosperityIncrease;
    public double section;
    public int[] itemsAdded;
    public int soldierIncrease;
    public int moraleEffect;

    public BuildingUpgradeEffect(int prosperityIncrease, double section, int[] itemsAdded, int soldierIncrease, int moraleEffect) {
        this.prosperityIncrease = prosperityIncrease;
        this.section = section;
        this.itemsAdded = itemsAdded;
        this.soldierIncrease = soldierIncrease;
        this.moraleEffect = moraleEffect;
    }

}

